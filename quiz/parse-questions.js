// Step 1: 按题号分割
const blocks = input.split(/\n(?=\d+\.\s*\()/);
const questions = [];

for (const block of blocks) {
  if (!/^\d+\.\s*\(/.test(block)) continue;

  const text = block.trim();
  const lines = text.split('\n').map(l => l.trim()).filter(l => l);

  // 提取题型
  const typeMatch = text.match(/[（(]([^）)]+)[）)]/);
  if (!typeMatch) continue;
  const rawType = typeMatch[1];
  let type = 'single';
  if (rawType.includes('多选')) type = 'multiple';
  else if (rawType.includes('判断')) type = 'truefalse';

  // 提取正确答案行
  const answerLine = lines.find(l => l.includes('正确答案'));
  if (!answerLine) continue;

  let answer;
  if (type === 'multiple') {
    // 格式: 正确答案:AD:选项1; 选项2;
    const ansMatch = answerLine.match(/正确答案:([A-E]+):/);
    if (ansMatch) {
      answer = ansMatch[1].split('').map(ch => String(ch.charCodeAt(0) - 65));
    } else {
      continue;
    }
  } else if (type === 'truefalse') {
    // 格式: 正确答案:对 或 正确答案:错
    if (/正确答案:对/.test(answerLine)) {
      answer = '0';
    } else {
      answer = '1';
    }
  } else {
    // 单选题: 正确答案:X:文字;
    const ansMatch = answerLine.match(/正确答案:([A-E]):/);
    if (ansMatch) {
      answer = String(ansMatch[1].charCodeAt(0) - 65);
    } else {
      continue;
    }
  }

  // 提取问题文本（从题号行之后到第一个"A."之前）
  const qMatch = text.match(/\d+\.\s*\([一-龥\w]+\)\s*([\s\S]*?)(?=A\.)/);
  if (!qMatch) continue;
  let question = qMatch[1]
    .replace(/\n{2,}/g, '\n')
    .replace(/\n/g, ' ')
    .replace(/\s+/g, ' ')
    .replace(/^[\d]+[\.、\s)]/, '') // Only strip leading "1." "1、" "1)" patterns
    .replace(/。。$/, '。') // Fix double period
    .trim();

  // 提取选项
  const options = [];
  const optLines = text.match(/[A-E]\.\s*([\s\S]*?)(?=(?:[A-E]\.|我的答案|$))/g);
  if (optLines) {
    for (const opt of optLines) {
      const optText = opt
        .replace(/^[A-E]\.\s*/, '')
        .replace(/\n{2,}/g, '\n')
        .replace(/\n/g, ' ')
        .replace(/\s+/g, ' ')
        .trim();
      if (optText) options.push(optText);
    }
  }

  // 判断题强制设选项
  if (type === 'truefalse') {
    options.length = 0;
    options.push('正确', '错误');
  }

  if (question && options.length >= 2) {
    questions.push({ type, question, options, answer, explanation: '' });
  }
}

// ============================================================
